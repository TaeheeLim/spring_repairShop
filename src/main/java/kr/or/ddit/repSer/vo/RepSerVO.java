package kr.or.ddit.repSer.vo;

public class RepSerVO {
	private String empNo;
	private String cusNo;
	private String serNo;
	private int repPay;
	private int repTm;
	public String getEmpNo() {
		return empNo;
	}
	public void setEmpNo(String empNo) {
		this.empNo = empNo;
	}
	public String getCusNo() {
		return cusNo;
	}
	public void setCusNo(String cusNo) {
		this.cusNo = cusNo;
	}
	public String getSerNo() {
		return serNo;
	}
	public void setSerNo(String serNo) {
		this.serNo = serNo;
	}
	public int getRepPay() {
		return repPay;
	}
	public void setRepPay(int repPay) {
		this.repPay = repPay;
	}
	public int getRepTm() {
		return repTm;
	}
	public void setRepTm(int repTm) {
		this.repTm = repTm;
	}
	
	
}
